public class Lesson {

    public static void main(String[] args) {
        // variables
        int number = 5; // assignment operator

        // single = assigns a value
        // different from ==, which compares a value

        // different assignment operators
        number += 6; // this is the same as number = number + 6
        number -= 3; // number = number - 3
        number *= 10; // number = number * 10
        number /= 2; // number = number / 2

        // increment and decrement operators
        number += 1;
        number++;

        number -= 1;
        number--;

        // boolean operators, evaluate to true or false
        // used in if statements

        // YOU CANNOT USE THESE FOR OBJECTS

        /*
         * numbers == 5; // equals numbers != 10; // not equals numbers <= 15; // less than or
         * equals numbers >= 20; // greater than or equals
         */
    }
}
