public class ForLoopDemo {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }

        // For Loops work like for loops
        // They go through the loop for
        // with initial, condition, and iterator
    }
}
